alldone.io is a simple JSON to-do list that:
<ul>
	<li>Has a simple JS interface</li>
	<li>Allows for unlimited child lists!</li>
	<li>Works on multiple devices<br />
		<ul>
			<li>Web and responsive mobile site</li>
			<li>Windows, Linux and OSX desktop applications coming very soon</li>
			<li>Android, iOS, Windows Phone and BlackBerry OS coming a bit later</li>
		</ul>
	<li>Allows sharing of lists between members and publishing lists as read-only and public (no login needed).</li>
	<li>Has dynamic items such as importing and updating GitHub issues into a child item automatically.</li>
	<li>Themeable</li>
	<li>Quickly syncs</li>
	<li>Has a built in JSON editor for fiddling, exporting and importing</li>
	<li>Allows for drag &amp; drop sorting</li>
	<li>Has a (primitive, currently) tagging system</li>
	<li>Has fields extra details with icons, dates and custom fields coming soon</li>
	<li>Is free :)</li>
</ul>

